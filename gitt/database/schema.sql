-- ---------------------------------------------------------------------
-- 4. REGLA DE NEGOCIO: un articulo no puede estar en 2 prestamos activos
-- ---------------------------------------------------------------------

CREATE OR REPLACE TRIGGER TRG_PRESTAMO_ACTIVO
BEFORE INSERT ON DETALLE_PRESTAMO
FOR EACH ROW
DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM DETALLE_PRESTAMO dp
    JOIN PRESTAMO p ON dp.ID_PRE = p.ID_PRE
    WHERE dp.ID_ART = :NEW.ID_ART
      AND p.EST_PRE IN ('ACTIVO','PENDIENTE');

    IF v_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20001,
            'El articulo ya tiene un prestamo activo. No puede prestarse nuevamente.');
    END IF;
END;
/

-- ---------------------------------------------------------------------
-- 5. TRIGGERS DE AUDITORIA AUTOMATICA (tablas criticas)
-- ---------------------------------------------------------------------

CREATE OR REPLACE TRIGGER TRG_AUD_ARTICULO
AFTER INSERT OR UPDATE OR DELETE ON ARTICULO
FOR EACH ROW
DECLARE
    v_accion VARCHAR2(20);
    v_id_reg NUMBER(10);
    v_usu    NUMBER(10);
BEGIN
    IF INSERTING THEN v_accion := 'INSERT'; v_id_reg := :NEW.ID_ART;
    ELSIF UPDATING THEN v_accion := 'UPDATE'; v_id_reg := :NEW.ID_ART;
    ELSE v_accion := 'DELETE'; v_id_reg := :OLD.ID_ART;
    END IF;

    BEGIN
        v_usu := TO_NUMBER(SYS_CONTEXT('USERENV','CLIENT_IDENTIFIER'));
    EXCEPTION WHEN OTHERS THEN v_usu := 1;
    END;

    INSERT INTO AUDITORIA (FEC_HOR, ACC_AUD, TAB_AUD, ID_REG_AUD, DES_AUD, ID_USU)
    VALUES (SYSTIMESTAMP, v_accion, 'ARTICULO', v_id_reg,
            'Operacion automatica sobre ARTICULO', NVL(v_usu,1));
END;
/

