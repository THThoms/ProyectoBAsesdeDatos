    CONSTRAINT FK_MANTENIMIENTO_USUARIO  FOREIGN KEY (ID_USU) REFERENCES USUARIO(ID_USU),
    CONSTRAINT CK_MAN_TIP CHECK (TIP_MAN IN ('PREVENTIVO','CORRECTIVO'))
);

CREATE TABLE MOVIMIENTO (
    ID_MOV       NUMBER(10) PRIMARY KEY,
    FEC_MOV      DATE DEFAULT SYSDATE NOT NULL,
    TIP_MOV      VARCHAR2(20) NOT NULL,
    DES_MOV      VARCHAR2(200),
    ID_UBI_ORI   NUMBER(10),
    ID_UBI_DES   NUMBER(10),
    ID_EST_ANT   NUMBER(10),
    ID_EST_NUE   NUMBER(10),
    ID_ART       NUMBER(10) NOT NULL,
    ID_USU       NUMBER(10) NOT NULL,
    CONSTRAINT FK_MOVIMIENTO_UBI_ORI  FOREIGN KEY (ID_UBI_ORI) REFERENCES UBICACION(ID_UBI),
    CONSTRAINT FK_MOVIMIENTO_UBI_DES  FOREIGN KEY (ID_UBI_DES) REFERENCES UBICACION(ID_UBI),
    CONSTRAINT FK_MOVIMIENTO_EST_ANT  FOREIGN KEY (ID_EST_ANT) REFERENCES ESTADO_ARTICULO(ID_EST),
    CONSTRAINT FK_MOVIMIENTO_EST_NUE  FOREIGN KEY (ID_EST_NUE) REFERENCES ESTADO_ARTICULO(ID_EST),
    CONSTRAINT FK_MOVIMIENTO_ARTICULO FOREIGN KEY (ID_ART)     REFERENCES ARTICULO(ID_ART),
    CONSTRAINT FK_MOVIMIENTO_USUARIO  FOREIGN KEY (ID_USU)     REFERENCES USUARIO(ID_USU),
    CONSTRAINT CK_MOV_TIP CHECK (TIP_MOV IN ('TRASLADO','PRESTAMO','DEVOLUCION','MANTENIMIENTO','BAJA','REGISTRO'))
