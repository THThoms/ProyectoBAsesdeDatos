    ID_USU        NUMBER(10) NOT NULL,
    ID_RESP       NUMBER(10) NOT NULL,
    CONSTRAINT FK_PRESTAMO_USUARIO     FOREIGN KEY (ID_USU)  REFERENCES USUARIO(ID_USU),
    CONSTRAINT FK_PRESTAMO_RESPONSABLE FOREIGN KEY (ID_RESP) REFERENCES RESPONSABLE(ID_RESP),
    CONSTRAINT CK_PRESTAMO_EST CHECK (EST_PRE IN ('PENDIENTE','ACTIVO','DEVUELTO','VENCIDO','CANCELADO'))
