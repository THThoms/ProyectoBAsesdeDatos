CREATE TABLE ROL (
    ID_ROL   NUMBER(10) PRIMARY KEY,
    NOM_ROL  VARCHAR2(30) NOT NULL,
    DES_ROL  VARCHAR2(80),
    CONSTRAINT UQ_ROL_NOM UNIQUE (NOM_ROL)
);

CREATE TABLE USUARIO (
    ID_USU   NUMBER(10) PRIMARY KEY,
    NOM_USU  VARCHAR2(20) NOT NULL,
    APE_USU  VARCHAR2(20) NOT NULL,
    COR_USU  VARCHAR2(40) NOT NULL,
    CLA_USU  VARCHAR2(100) NOT NULL,
    EST_USU  VARCHAR2(15) DEFAULT 'ACTIVO' NOT NULL,
    ID_ROL   NUMBER(10) NOT NULL,
    CONSTRAINT UQ_USUARIO_COR UNIQUE (COR_USU),
    CONSTRAINT FK_USUARIO_ROL FOREIGN KEY (ID_ROL) REFERENCES ROL(ID_ROL),
    CONSTRAINT CK_USUARIO_EST CHECK (EST_USU IN ('ACTIVO','INACTIVO','BLOQUEADO'))
);

CREATE TABLE DEPARTAMENTO (
    ID_DEP   NUMBER(10) PRIMARY KEY,
    NOM_DEP  VARCHAR2(40) NOT NULL,
    DES_DEP  VARCHAR2(120)
);

CREATE TABLE UBICACION (
    ID_UBI   NUMBER(10) PRIMARY KEY,
    NOM_UBI  VARCHAR2(40) NOT NULL,
    TIP_UBI  VARCHAR2(20) NOT NULL,
    DES_UBI  VARCHAR2(120),
    ID_DEP   NUMBER(10) NOT NULL,
    CONSTRAINT FK_UBICACION_DEPARTAMENTO FOREIGN KEY (ID_DEP) REFERENCES DEPARTAMENTO(ID_DEP),
    CONSTRAINT CK_UBICACION_TIP CHECK (TIP_UBI IN ('LABORATORIO','AULA','OFICINA','ALMACEN'))
);

CREATE TABLE CATEGORIA (
    ID_CAT   NUMBER(10) PRIMARY KEY,
    NOM_CAT  VARCHAR2(25) NOT NULL,
    DES_CAT  VARCHAR2(120),
    CONSTRAINT UQ_CATEGORIA_NOM UNIQUE (NOM_CAT)
);

CREATE TABLE ESTADO_ARTICULO (
    ID_EST   NUMBER(10) PRIMARY KEY,
    NOM_EST  VARCHAR2(20) NOT NULL,
    DES_EST  VARCHAR2(120),
    CONSTRAINT UQ_ESTADO_NOM UNIQUE (NOM_EST)
);

CREATE TABLE RESPONSABLE (
    ID_RESP   NUMBER(10) PRIMARY KEY,
    NOM_RESP  VARCHAR2(30) NOT NULL,
    APE_RESP  VARCHAR2(30) NOT NULL,
    TIP_RESP  VARCHAR2(20) NOT NULL,
    COR_RESP  VARCHAR2(60),
    TEL_RESP  VARCHAR2(15),
    CONSTRAINT CK_RESP_TIP CHECK (TIP_RESP IN ('DOCENTE','ESTUDIANTE','ADMINISTRATIVO'))
);

CREATE TABLE ARTICULO (
    ID_ART   NUMBER(10) PRIMARY KEY,
    COD_BAR  VARCHAR2(30),
    COD_INS  VARCHAR2(30),
    NOM_ART  VARCHAR2(60) NOT NULL,
    DES_ART  VARCHAR2(200),
    MAR_ART  VARCHAR2(30),
    MOD_ART  VARCHAR2(30),
    SER_ART  VARCHAR2(40),
    FEC_REG  DATE DEFAULT SYSDATE NOT NULL,
    IMG_ART  VARCHAR2(120),
    ACT_ART  CHAR(1) DEFAULT 'S' NOT NULL,
    ID_CAT   NUMBER(10) NOT NULL,
    ID_EST   NUMBER(10) NOT NULL,
    ID_UBI   NUMBER(10) NOT NULL,
    ID_RESP  NUMBER(10),
    CONSTRAINT UQ_ARTICULO_BAR UNIQUE (COD_BAR),
    CONSTRAINT UQ_ARTICULO_INS UNIQUE (COD_INS),
    CONSTRAINT FK_ARTICULO_CATEGORIA   FOREIGN KEY (ID_CAT)  REFERENCES CATEGORIA(ID_CAT),
    CONSTRAINT FK_ARTICULO_ESTADO      FOREIGN KEY (ID_EST)  REFERENCES ESTADO_ARTICULO(ID_EST),
    CONSTRAINT FK_ARTICULO_UBICACION   FOREIGN KEY (ID_UBI)  REFERENCES UBICACION(ID_UBI),
    CONSTRAINT FK_ARTICULO_RESPONSABLE FOREIGN KEY (ID_RESP) REFERENCES RESPONSABLE(ID_RESP),
    CONSTRAINT CK_ARTICULO_ACT CHECK (ACT_ART IN ('S','N'))
